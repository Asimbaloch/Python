#Built-in data types in Python:
#1. Numbers(int, float, complex, bool)
#2. Text(str, unicode)
#3. Lists(tuple)
#4. Dictionaries
#5. Sets
#6. Booleans
#9. None

#Numbers
#- Integers: whole numbers
#- Floating point numbers:decimal numbers
#- Complex numbers: combination of real and imaginary numbers
#- Boolean: True or False

#Text
#- Strings: collection of characters
#- Unicode strings: support for any character from any language

#Lists
#- Ordered collection of items
#- Items can be of any data type
#- Indexing starts from 0
#- Slicing: accessing multiple items at once

#Dictionaries
#- Unordered collection of key-value pairs
#- Keys must be unique
#- Accessing items using keys

#Sets
#- Unordered collection of unique items
#- Accessing items using index or loop

#Booleans
#- True or False

#Bytes
#- Immutable sequence of bytes

#Byte Arrays
#- Immutable sequence of bytes

#None
#- Special value representing absence of a value


